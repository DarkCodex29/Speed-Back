package com.hochschild.speed.back.model.domain.speed.utils;

public class DatosDocumentoLegalDTS {

	private String titulo;
	private String contraparte;
	private String fechaInicio;
	private String fechaFin;
	private String solicitante;
	private String responsable;
	private String estado;
	private String proposito;
	private Integer numeroAdendas;
	
	public String getTitulo() {
		return titulo;
	}
	public void setTitulo(String titulo) {
		this.titulo = titulo;
	}
	public String getContraparte() {
		return contraparte;
	}
	public void setContraparte(String contraparte) {
		this.contraparte = contraparte;
	}
	public String getFechaInicio() {
		return fechaInicio;
	}
	public void setFechaInicio(String fechaInicio) {
		this.fechaInicio = fechaInicio;
	}
	public String getFechaFin() {
		return fechaFin;
	}
	public void setFechaFin(String fechaFin) {
		this.fechaFin = fechaFin;
	}
	public String getSolicitante() {
		return solicitante;
	}
	public void setSolicitante(String solicitante) {
		this.solicitante = solicitante;
	}
	public String getResponsable() {
		return responsable;
	}
	public void setResponsable(String responsable) {
		this.responsable = responsable;
	}
	public String getEstado() {
		return estado;
	}
	public void setEstado(String estado) {
		this.estado = estado;
	}
	public String getProposito() {
		return proposito;
	}
	public void setProposito(String proposito) {
		this.proposito = proposito;
	}
	public Integer getNumeroAdendas() {
		return numeroAdendas;
	}
	public void setNumeroAdendas(Integer numeroAdendas) {
		this.numeroAdendas = numeroAdendas;
	}
}